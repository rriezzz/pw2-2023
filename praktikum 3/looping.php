<?php

// Mencetak angka 1 sampai 10 menggunakan for
for ($i = 1; $i < 10; $i++) {
    echo "<h1>$i</h1>";
}

// Membuat sebuah array
$buah = ["apel", "mangga", "jeruk", "pisang"];

// Menggunakan foreach untuk menampilkan setiap elemen pada array
foreach ($buah as $b) {
    echo "<h1>$b</h1>";
}

?>